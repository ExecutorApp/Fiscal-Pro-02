// Função para normalizar CNAE (remove pontuação)
export const normalizeCNAE = (cnae: string): string => {
  return cnae.replace(/[^0-9]/g, '');
};

// Função para normalizar texto (remove acentos, converte para minúsculas)
export const normalizeText = (text: string): string => {
  return text
    .toLowerCase()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
};

// Função para buscar CNAEs
export const searchCNAEs = (query: string): CNAE[] => {
  if (!query.trim()) return CNAES;
  
  const normalizedQuery = normalizeText(query);
  const normalizedCNAEQuery = normalizeCNAE(query);
  
  return CNAES.filter(cnae => {
    const matchesCNAE = normalizeCNAE(cnae.cnae).includes(normalizedCNAEQuery);
    const matchesDescription = normalizeText(cnae.descricao).includes(normalizedQuery);
    return matchesCNAE || matchesDescription;
  });
};